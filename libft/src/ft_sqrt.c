#include "libft.h"

int	ft_sqrt(int nb)
{
	int a;

	a = 0;
	while (a * a <= nb)
	{
		if (a * a == nb)
		{
			nb = a;
			return (nb);
		}
		a++;
	}
	return (0);
}
